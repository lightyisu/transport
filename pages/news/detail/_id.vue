出口俄罗斯货物须提供6位HS CODE

根据俄罗斯海关要求，所有至俄罗斯以及在此中转的货物必须提供6位HS CODE。如果是多种货物混装，涉及到不同的HS CODE，请按照HS CODE分开发送货物信息，避免申报不全，引起海关罚金。

出口英国货物要有付款凭证和CE认证

1.通过FedEx寄往英国的货件必须提供付款凭证，如没有提供，将被退件，走货前要附带好随货资料！

2.出口英国无CE认证可能有被海关扣留和查处的风险，主要是针对电子、机械类产品“CE”标志是一种安全认证标志，被视为制造商打开并进入欧洲市场的护照。CE代表欧洲统一（CONFORMITE EUROPEENNE）。在欧盟市场“CE”标志属强制性认证标志，不论是欧盟内部企业生产的产品，还是其他***生产的产品，要想在欧盟市场上自由流通，就必须加贴“CE”标志，以表明产品符合欧盟《技术协调与标准化新方法》指令的基本要求。这是欧盟法律对产品提出的一种强制性要求。

出口到德国的机电产品应注意以下几点

1．德方对进口一般机电产品（如刀具、农机具、机床、轧钢设备、农业机械、拖拉机、办公机械、电子电气技术产品等）没有贸易限制规定，也不需要进口许可。但对自行车进口有贸易限制，德进口商需向德国政府部门事先申领进口监督文件后方可进口。

2．德方对进口商品的税赋包括关税和进口增值税，个别商品还要额外交纳反倾销税。德海关关税视商品项目和税则不同有详细的关税税率规定，比如加关税为3.2%；彩电正常关税为14%，我国不同尺寸的彩电均计征44.6%反倾销税，包括42厘米及以下，42-52厘米之间、52-72厘米之间及72厘米以上。进口增值税一般为16%，该税种的计征范围是从欧盟以外的第三国进口的商品。

3．德国实施欧盟统一的安全标准- CE标志。

4．我国航运公司对进出口货物的运价设定不公平。以40尺集装箱中国港口到欧洲航线的运价为例，出口货物每货柜2400-2600美元，进口货物600-700美元即可。对出口产品的成本核算很不利，从而影响成交和出运。


